package com.hensley.ufc.pojo.parse;

public class ScorePartialParse {
	private String fightName;
	private String fightUrl;
	private String fightId;
	
	public ScorePartialParse() {
		
	}

	/**
	 * @return the fightName
	 */
	public String getFightName() {
		return fightName;
	}

	/**
	 * @param fightName the fightName to set
	 */
	public void setFightName(String fightName) {
		this.fightName = fightName;
	}

	/**
	 * @return the fightUrl
	 */
	public String getFightUrl() {
		return fightUrl;
	}

	/**
	 * @param fightUrl the fightUrl to set
	 */
	public void setFightUrl(String fightUrl) {
		this.fightUrl = fightUrl;
	}

	/**
	 * @return the fightId
	 */
	public String getFightId() {
		return fightId;
	}

	/**
	 * @param fightId the fightId to set
	 */
	public void setFightId(String fightId) {
		this.fightId = fightId;
	}
	
	
}
